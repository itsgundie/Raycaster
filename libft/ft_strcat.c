#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *ss1;

	ss1 = s1;
	while (*ss1 != '\0')
		ss1++;
	while (*s2 != '\0')
		*ss1++ = *s2++;
	*ss1 = '\0';
	return (s1);
}
