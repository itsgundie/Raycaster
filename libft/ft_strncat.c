#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *ss1;

	ss1 = s1;
	while (*ss1 != '\0')
		ss1++;
	while (n-- > 0 && *s2 != '\0')
		*ss1++ = *s2++;
	*ss1 = '\0';
	return (s1);
}
