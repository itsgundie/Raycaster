#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < -2147483647)
	{
		ft_putnbr(-2);
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n / 10 > 0)
	{
		ft_putnbr(n / 10);
	}
	ft_putchar(n % 10 + 48);
}
