void	delete_data(t_wolfec *w)
{
	t_pars_list *tmp;

	while (w->params_list != NULL)
	{
		tmp = w->params_list->next;
		free(w->params_list);
		w->params_list = tmp;
	}
	free(w);
}

int		main(int argc, char **argv)
{
    int fd;
  	t_wolfec *w;

    fd = 0;
    if (argc != 2 || (fd = open(argv[1], O_RDONLY)) < 0)
		ft_error("\033[0;32musage: ./wolf3d map");
    w = preparation();
	if (file_parser(w, fd))
		create_map(w);
	w->player.pos.x = 5 * TILE_SIZE + 7;//5 * TILE_SIZE + 13;
	w->player.pos.y = 13 * TILE_SIZE + 5;//13 * TILE_SIZE + 13;
	init_sdl(w);
	predraw(w);
	texturembo(w);
  // SDL_Delay(5000);
	while (w->event.type != SDL_QUIT)
	{
		// set_mouse(w);
		events(w);
		if (w->event.type == SDL_QUIT)
			break ;
		move(w);
		update(w);
		render_it(w);
	}
	return (0);
}